import type { PipelineRun, HistoricalRun, PipelineSlo } from '@/lib/types';
import { hoursAgo, isoSeconds } from '@/lib/time';
import { rng, randInt } from '@/lib/seed';

/** Synthesize 14 historical runs with status weighting. */
function buildHistory(ns: string, runs: number, failProb: number): HistoricalRun[] {
  const r = rng(`history-${ns}`);
  const out: HistoricalRun[] = [];
  for (let i = runs - 1; i >= 0; i--) {
    const status = r() < failProb ? 'failed' : 'success';
    out.push({
      startedAt: isoSeconds(hoursAgo(i * 6 + 0.5)),
      durationMs: randInt(r, 22_000, 180_000),
      status,
    });
  }
  return out;
}

function slo(
  successRate: number,
  p95Actual: number,
  p95Target: number,
  freshnessHit: number,
  mttd: number,
  mttr: number,
  failures30d: number,
): PipelineSlo {
  return {
    successRate30d: successRate,
    successRateTarget: 0.99,
    p95DurationMsActual: p95Actual,
    p95DurationMsTarget: p95Target,
    freshnessHitRate30d: freshnessHit,
    mttdMinutes: mttd,
    mttrMinutes: mttr,
    failureCount30d: failures30d,
  };
}

